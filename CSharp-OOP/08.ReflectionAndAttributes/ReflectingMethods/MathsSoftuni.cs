using System;
using System.Collections.Generic;
using System.Text;

namespace ReflectingMethods
{
    public class MathsSoftuni
    {
        public int Add(int a, int b)
        {
            return a + b;
        }
        public static int Multiply(int a, int b)
        {
            return a * b;
        }
        private int Divide(int a, int b)
        {
            return a / b;
        }
    }
}
